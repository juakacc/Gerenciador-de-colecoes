package br.com.gerenciador.exceptions;

/**
 * Exceção para sinalizar que uma trilogia não existe ou não
 * foi encontrada, para que não seja necessária a 
 * verificação se a trilogia que está sendo manipulada é null
 * ou não.
 * */
public class TrilogiaNotFoundException extends Exception {

	private static final long serialVersionUID = 8406853329245625158L;

	/**
	 * Constrói um nova exceção com a mensagem padrão.
	 * */
	public TrilogiaNotFoundException() {
		super("Trilogia não foi encontrada!");
	}
	
	/**
	 * Constrói uma nova exceção com uma mensagem 
	 * de detalhe especificada.
	 * @param mensagem
	 * */
	public TrilogiaNotFoundException(String mensagem) {
		super(mensagem);
	}
}