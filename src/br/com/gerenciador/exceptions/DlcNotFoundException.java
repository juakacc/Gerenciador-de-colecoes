package br.com.gerenciador.exceptions;

/**
 * Exceção para sinalizar que uma DLC não existe ou não
 * foi encontrada, para que não seja necessária a 
 * verificação se a DLC que está sendo manipulada é null
 * ou não.
 * */
public class DlcNotFoundException extends Exception {

	private static final long serialVersionUID = 1L;

	/**
	 * Constrói um nova exceção com a mensagem padrão.
	 * */
	public DlcNotFoundException() {
		super("A DLC não foi encontrada!");
	}

	/**
	 * Constrói uma nova exceção com uma mensagem 
	 * de detalhe especificada.
	 * @param mensagem
	 * */
	public DlcNotFoundException(String message) {
		super(message);
	}
}